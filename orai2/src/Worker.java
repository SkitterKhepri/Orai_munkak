public class Worker {


    private String name;
    private String city;
    private String address;
    private String salary;
    private String bonus;
    private String born;
    private String hired;

    public String getName(){return name;}
    public String getCity(){return city;}
    public String getAddress(){return address;}
    public String getSalary(){return salary;}
    public String getBonus(){return bonus;}
    public String getBorn(){return born;}
    public String getHired(){return hired;}

    public void setName(String text){this.name = text;}
    public void setCity(String text){this.city = text;}
    public void setAddress(String text){this.address = text;}
    public void setSalary(String text){this.salary = text;}
    public void setBonus(String text){this.bonus = text;}
    public void setBorn(String text){this.born = text;}
    public void setHired(String text){this.hired = text;}



}
